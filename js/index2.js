function removeSymbolsFromArray(array, ...symbols) {
  return array.filter((element) => !isElementEqualToSymbols(element, ...symbols));
}

function isElementEqualToSymbols(element, ...symbols) {
  return symbols.some((symbol) => element === symbol);
}

function isElementMultipleOf(element, ...multiples) {
  return multiples.some((multiple) => element % multiple === 0);
}

function isElementCapitalLetter(element) {
  return /[A-Z]/g.test(element);
}

